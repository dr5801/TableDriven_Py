# returns true if the character is a digit
def is_digit(character):
    return character.isdigit()

# returns true if the character is a minus sign
def is_minus(character):
    return character == '-'

# returns true fi the character is a plus sign
def is_plus(character):
    return character == '+'

# returns true if the character is a period (decimal)
def is_period(character):
    return character == '.'